N, S = gets.chomp.split.map(&:to_i)
A = gets.chomp.split.map(&:to_i)
P = gets.chomp.split.map(&:to_i)

sum = 0

N.times do |i|
    N.times do |j|
        if A[i] + P[j] == S
            sum += 1
        end
    end
end

puts sum

