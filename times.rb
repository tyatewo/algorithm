# 合計が400円になる買い方は以下の3通りです。
# 1つ目のリンゴ、2つ目のパイナップルを購入すると、100円 + 300円 = 400円となります。
# 2つ目のリンゴ、2つ目のパイナップルを購入すると、100円 + 300円 = 400円となります。
# 3つ目のリンゴ、1つ目のパイナップルを購入すると、130円 + 270円 = 400円となります。
